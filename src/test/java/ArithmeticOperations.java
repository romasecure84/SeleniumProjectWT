public class ArithmeticOperations {
    public static void main(String[] args) {
        int i = 5;
        System.out.println("Old i = " + i);
        System.out.println("New i = " + ++i);
        System.out.println("New new i = " + ++i);
        System.out.println("i = " + i);
        i += 5;
        System.out.println("i = " + i);
        i /=4;
        System.out.println("i = " + i);
    }
}
